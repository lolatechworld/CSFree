# Create a Python script that calculates the area of a rectangle 
# based on the provided length and width. 
# Your script should take the length and width as inputs, 
# multiply them together to find the area, 
# and then print the result.
print("Calculate the area of a rectangle")
length = int(input("length: "))
width = int(input("width: "))
area = (length * width)
print(f"Area of rectangle is: {area}")

